static inline uint8_t gf_mul(uint8_t a, uint8_t b) {
    if (a == 0 || b == 0) return 0;
    return gf_exp[gf_log[a] + gf_log[b]];
}

static inline uint8_t gf_inv(uint8_t a) {
    if (a == 0) return 0;
    return gf_exp[255 - gf_log[a]];
}

// ──── Encode: systematic RS ────
void HERMES_FEC_Encode(uint8_t block[RS_N]) {
    // Compute remainder of data / generator
    uint8_t rem[RS_T];
    memset(rem, 0, RS_T);

    for (uint8_t i = 0; i < RS_K; i++) {
        uint8_t feedback = block[i] ^ rem[0];
        // Shift remainder left
        for (uint8_t j = 0; j < RS_T - 1; j++) {
            rem[j] = rem[j + 1] ^ gf_mul(feedback, gen_poly[RS_T - 1 - j]);
        }
        rem[RS_T - 1] = gf_mul(feedback, gen_poly[0]);
    }

    // Append parity
    memcpy(block + RS_K, rem, RS_T);
}

// ──── Decode: syndrome-based with Berlekamp-Massey ────
int8_t HERMES_FEC_Decode(uint8_t block[RS_N]) {
    // 1. Compute syndromes S[0..31]
    uint8_t syn[RS_T];
    bool all_zero = true;
    for (uint8_t i = 0; i < RS_T; i++) {
        uint8_t s = 0;
        for (uint8_t j = 0; j < RS_N; j++) {
            s = gf_mul(s, gf_exp[i]) ^ block[j];
        }
        syn[i] = s;
        if (s != 0) all_zero = false;
    }
    if (all_zero) return 0; // No errors

    // 2. Berlekamp-Massey to find error locator polynomial
    uint8_t sigma[RS_T + 1]; // error locator
    uint8_t old_sigma[RS_T + 1];
    memset(sigma, 0, sizeof(sigma));
    memset(old_sigma, 0, sizeof(old_sigma));
    sigma[0] = 1;
    old_sigma[0] = 1;

    uint8_t L = 0;
    for (uint8_t n = 0; n < RS_T; n++) {
        uint8_t delta = syn[n];
        for (uint8_t j = 1; j <= L; j++) {
            delta ^= gf_mul(sigma[j], syn[n - j]);
        }

        // Shift old_sigma
        uint8_t tmp[RS_T + 1];
        memset(tmp, 0, sizeof(tmp));
        for (uint8_t j = 0; j <= RS_T; j++) {
            if (j > 0) tmp[j] = old_sigma[j - 1];
        }

        if (delta != 0) {
            uint8_t new_sigma[RS_T + 1];
            for (uint8_t j = 0; j <= RS_T; j++) {
                new_sigma[j] = sigma[j] ^ gf_mul(delta, tmp[j]);
            }
            if (2 * L <= n) {
                uint8_t inv_delta = gf_inv(delta);
                for (uint8_t j = 0; j <= RS_T; j++) {
                    old_sigma[j] = gf_mul(sigma[j], inv_delta);
                }
                L = n + 1 - L;
            } else {
                memcpy(old_sigma, tmp, sizeof(old_sigma));
            }
            memcpy(sigma, new_sigma, sizeof(sigma));
        } else {
            memcpy(old_sigma, tmp, sizeof(old_sigma));
        }
    }

    if (L > RS_T / 2) return -1; // Too many errors

    // 3. Chien search: find error positions
    uint8_t err_pos[RS_T / 2];
    uint8_t err_count = 0;
    for (uint8_t i = 0; i < RS_N; i++) {
        uint8_t eval = 0;
        uint8_t x_inv = gf_exp[255 - i]; // α^(-i)
        uint8_t power = 1;
        for (uint8_t j = 0; j <= L; j++) {
            eval ^= gf_mul(sigma[j], power);
            power = gf_mul(power, x_inv);
        }
        if (eval == 0) {
            if (err_count >= RS_T / 2) return -1;
            err_pos[err_count++] = (RS_N - 1 - i);
        }
    }

    if (err_count != L) return -1; // Degree mismatch

    // 4. Forney algorithm: compute error magnitudes
    // Error evaluator Ω(x) = S(x) * σ(x) mod x^(2t)
    uint8_t omega[RS_T];
    memset(omega, 0, RS_T);
    for (uint8_t i = 0; i < RS_T; i++) {
        uint8_t v = 0;
        for (uint8_t j = 0; j <= i && j <= L; j++) {
            v ^= gf_mul(sigma[j], syn[i - j]);
        }
        omega[i] = v;
    }

    // σ'(x) = formal derivative of σ
    for (uint8_t k = 0; k < err_count; k++) {
        uint8_t pos = err_pos[k];
        if (pos >= RS_N) return -1;

        uint8_t x_inv = gf_exp[255 - (RS_N - 1 - pos)];
        uint8_t x_val = gf_exp[RS_N - 1 - pos];

        // Evaluate Ω at x_inv
        uint8_t omega_val = 0;
        uint8_t power = 1;
        for (uint8_t j = 0; j < RS_T; j++) {
            omega_val ^= gf_mul(omega[j], power);
            power = gf_mul(power, x_inv);
        }

        // Evaluate σ'(x_inv) — only odd terms contribute
        uint8_t sigma_deriv = 0;
        power = 1;
        for (uint8_t j = 1; j <= L; j += 2) {
            sigma_deriv ^= gf_mul(sigma[j], power);
            if (j + 2 <= L) power = gf_mul(power, gf_mul(x_inv, x_inv));
        }

        if (sigma_deriv == 0) return -1; // Shouldn't happen

        uint8_t magnitude = gf_mul(omega_val, gf_inv(sigma_deriv));
        // For conventional RS, also multiply by x_val
        magnitude = gf_mul(magnitude, x_val);

        block[pos] ^= magnitude;
    }

    return (int8_t)err_count;
}

